# ========== 过滤函数 ==========

def contains_sensitive_word(text: str, keywords: Optional[Set[str]] = None) -> bool:
    """
    检查文本是否包含敏感词
    
    Args:
        text: 待检查文本
        keywords: 关键词集合，默认使用 DEEPSEEK_KEYWORDS
        
    Returns:
        是否包含敏感词
        
    """

    if not text:
        return False
    
    if keywords is None:
        keywords = DEEPSEEK_KEYWORDS
    
    text = str(text)
    return any(keyword in text for keyword in keywords)


def filter_text(
    text: str,
    keywords: Optional[Set[str]] = None,
    replace_char: str = "*"
) -> str:
    """
    过滤文本中的敏感词
    
    Args:
        text: 原始文本
        keywords: 关键词集合，默认使用 DEEPSEEK_KEYWORDS
        replace_char: 替换字符
        
    Returns:
        过滤后的文本
        
    """

    if not text:
        return text
    
    if keywords is None:
        keywords = DEEPSEEK_KEYWORDS
    
    result = str(text)
    for keyword in keywords:
        if keyword in result:
            result = result.replace(keyword, replace_char * len(keyword))
    
    return result


def is_official_related(text: str) -> bool:
    """
    检查文本是否与官方部门相关
    
    Args:
        text: 待检查文本
        
    Returns:
        是否包含官方关键词
    """
    return contains_sensitive_word(text, OFFICIAL_KEYWORDS)


def is_risk_related(text: str) -> bool:
    """
    检查文本是否与风险相关
    
    Args:
        text: 待检查文本
        
    Returns:
        是否包含风险关键词
    """
    return contains_sensitive_word(text, RISK_KEYWORDS)


def extract_keywords(text: str, keywords: Set[str]) -> List[str]:
    """
    从文本中提取匹配的关键词
    
    Args:
        text: 待检查文本
        keywords: 关键词集合
        
    Returns:
        匹配的关键词列表
        
    """

    if not text:
        return []
    
    text = str(text)
    return [kw for kw in keywords if kw in text]
